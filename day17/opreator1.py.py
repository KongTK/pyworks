# 산술 연산자
print("10 + 4 =", 10 + 4)
print("10 - 4 =", 10 - 4)
print("10 * 4 =", 10 * 4)
print("10 / 4 =", 10 / 4)
print("10 // 4 =", 10 // 4) # 몫
print("10 % 4 =", 10 % 4) # 나머지
print("4 ** 4 =", 4 ** 4) # 거듭제곱

# 성적의 총점과 평균
# 수학 - 80점, 영어 - 85점
math_score = 80 #js는 mathScore, 파이썬은 math_score
eng_score = 85

sum = math_score + eng_score
avg = sum / 2

print("합계 : ", str(sum) + "점")
# ,사용시 sum과 점이 띄어져 80 점이라고 표시되므로 대신 + 사용
print("합계 : ", avg, "점")

